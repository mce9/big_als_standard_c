#include <stdio.h>

int main(void) {

	/*
	declare integer and float variables
	*/
	int intVal;
	float floatVal;

	// initialize them
	intVal = 3;
	floatVal = 3.14;

	printf("The value of the integer variable is %d", intVal);
	printf("\nAnd the value of the float variable is %f\n", floatVal);

	return 0;
}
