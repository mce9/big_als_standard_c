#include <stdio.h>

int main(void) {
	printf("%s\n", "Goonies never say die");
	printf("Klaata %s nikto\n", "barada");

	/* character constants are enclosed in single quotes */

	printf("%c%c, a bear in his natural habitat: a %s\n\n", 'A', 'h', "Studebaker");
	printf("The value of PI is approximately %f\n", 3.14159);
	printf("Euler's number is more or less equal to %f\n", 2.718282);

	return 0;
}
