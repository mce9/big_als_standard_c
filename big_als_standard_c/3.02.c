#include <stdio.h>

int main(void) {

	int intVar = 5.4;
	float floatVar = 5;

	printf("Assign 5.4 to the integer variable intVar\n");
	printf("intVar now contains the value %d\n", intVar);
	printf("Assign 5 to the floating point variable floatVar\n");
	printf("floatVar now contains the value %f\n", floatVar);

	return 0;
}
